#!/usr/bin/env python3
"""
This module is a simple demonstration of a Python script that prints "Hello, World!" to the standard output.
It includes a main function and checks if the script is run as the main module.
"""

def hello_world():
    """
    This function prints "Hello, World!" to the standard output.
    """
    print("Hello, World!")

if __name__ == "__main__":
    # Call the function only if this module is executed as the main script.
    hello_world()
